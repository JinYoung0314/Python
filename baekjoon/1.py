# 간단한 수학 연산

# 백준 1번
# 첫째 줄에 A와 B 입력 받은 다음, A+B를 출력하기
a, b = map(int, input().split())
print(a + b)

# 백준 2번
# 첫째 줄에 A와 B 입력 받은 다음, A-B를 출력하기
a, b = map(int, input().split())
print(a - b)

# 백준 3번
# 첫째 줄에 A와 B 입력 받은 다음, A/B를 출력하기
a, b = map(int, input().split())
print(a / b)

# # 백준 4번
# # 첫째 줄에 A와 B를 입력 받은 다음, A*B를 출력하기
a, b = map(int, input().split())
print(a * b)

# 백준 1번
# A와 B 입력 받은 다음, A + B를 출력하기
a = int(input("a를 입력하세요: "))
b = int(input("b를 입력하세요: "))
print(a + b)
