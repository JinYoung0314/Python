# print() => Hello 출력하기

print("Hello")

# print() = > Hello World 출력하기

print("Hello World")

# print() => Hello / World를 2줄로 나누어서 출력하기

print("Hello")
print("World")

# print() => 'Hello' 출력하기

print("'Hello'")

# print() => "Hello World" 출력하기

print('"Hello World"')

# print() => "!@#$%^&*()' 출력하기

print('\"!@#$%^&*()\'') # \'  \"를 사용하여 푸니까 되더라! \" -> " , \' -> ' 로 출력됨!

# print() => "C:\Download\'hello'.py" 출력하기

print('\"C:\Download\\\'hello\'.py\"')

# print() => print("Hello\nWorld") 출력하기

print('print("Hello\\nWorld")')
